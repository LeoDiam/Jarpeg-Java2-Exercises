
public class Devilkin {
    private float astrofel;
    public static int acheulean = 0;

    public Devilkin() {
        this.astrofel = 83.24F;
        acheulean++;
    }

    public static int getAcheulean() {
        return acheulean * 5;
    }

    public float getAstrofel() {
        return astrofel;
    }
}
