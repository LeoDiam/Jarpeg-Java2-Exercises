//Δημιουργήστε την δημόσια κλάση com.leodiam.java2Solutions.ex3.Kushshu η
//        οποία να περιέχει την δημόσια στατική μέθοδο κλάσης unsoured η
//        οποία δε δέχεται κανένα όρισμα και δεν επιστρέφει κανένα αποτέλεσμα.
//        Χρησιμοποιήστε τη μέθοδο αυτή για να εκτελέσετε τις εξής λειτουργίες:
//        Δημιουργήστε ένα αντικείμενο της κλάσης com.leodiam.java2Solutions.ex3.Upgush με όνομα oiler
//        χρησιμοποιώντας τον κατασκευαστή που δε δέχεται ορίσματα. Χρησιμοποιώντας
//        το αντικείμενο oiler καλέστε πρώτα τη μέθοδο getBegrime και στη
//        συνέχεια τη μέθοδο getDaubingly. Δημιουργήστε κατόπιν ένα δεύτερο
//        αντικείμενο της κλάσης com.leodiam.java2Solutions.ex3.Upgush με όνομα pituite καλώντας τον κατασκευαστή
//        που δέχεται ορίσματα. Δώστε την τιμή 'd' στο πρώτο όρισμα και
//        την τιμή 'p' στο δεύτερο όρισμα. Χρησιμοποιώντας το αντικείμενο
//        pituite καλέστε πρώτα τη μέθοδο setBegrime με όρισμα την τιμή
//        'y' και στη συνέχεια τη μέθοδο setDaubingly με όρισμα την τιμή
//        'c'. Χρησιμοποιώντας το αντικείμενο oiler καλέστε τη μέθοδο anice.
//        Τέλος καλέστε τη μέθοδο κλάσης idyllic.
public class Kushshu {
    public static void unsoured() {
        Upgush oiler = new Upgush();
        oiler.getBegrime();
        oiler.getDaubingly();
        Upgush pituite = new Upgush('d', 'p');
        pituite.setBegrime('y');
        pituite.setDaubingly('c');
        oiler.anice();
        Upgush.idyllic();

    }

}
